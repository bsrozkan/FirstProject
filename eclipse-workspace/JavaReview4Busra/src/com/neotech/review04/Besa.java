package com.neotech.review04;

public class Besa {

	// A method with NO parameters /no return type
	void addTwoNumbers() {
		int result = 2 + 5;

	}

	// A method with 2 parameters / no return type
	void addTwoNumbersThatIGive(int a, int b) {

		int result = a + b;
	}

	// A method with NO parameters but with return type
	int addTwoNumbersAndGiveMeTheResult() {

		int result = 2 + 5;

		return result;

	}
	
	//A method with 2 parameters and with return type 
	int addTwoNumbersGiveMeSum(int num1, int num2) {

		int result = num1 + num2;
		return result;
	}

}
